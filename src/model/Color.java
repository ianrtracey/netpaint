package model;

public class Color {
		
		int red, blue, green;
		
		public Color(int red, int blue, int green) {
			this.red   = red;
			this.blue  = blue;
			this.green = green;
		}
		
		public int getRed() {
			return this.red;
		}
		
		public int getBlue() {
			return this.blue;
		}
		
		public int getGreen() {
			return this.green;
		}

}
